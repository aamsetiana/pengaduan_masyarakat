    private void refreshTablepengaduan(){

    tml = new DefaultTableModel(null,
    new Object[] {"id pengaduan","tangggal","nik","pengaduan","gambar","status"});
    tblpengaduan.setModel(tml);
    tml.getDataVector().removeAllElements();
    try{
        PreparedStatement s= con.prepareStatement("SELECT * FROM pengaduan");
        ResultSet r = s.executeQuery();
        while(r.next()){
        Object[] data ={
        r.getString(1),
        r.getString(2),
        r.getString(3),
        r.getString(4),
        r.getString(5),
        r.getString(6)
        };
        tml.addRow(data);
        }
    }catch(Exception e){
    System.out.print("ERROR KUERI KE DATABAE:\n"+ e +"\n\n");
    }
}
    
