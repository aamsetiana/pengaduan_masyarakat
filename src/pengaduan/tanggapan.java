    private void id_otomatis(){
    try{
        Connection c = con;
        Statement s = c.createStatement();
        String sql = "SELECT * FROM tanggapan ORDER BY id_tanggapan DESC";
        ResultSet r = s.executeQuery(sql);
        if (r.next()){
            String No = r.getString("id_pengaduan");
            String otomatis = "" +(Integer.parseInt(No)+1);
            txtidtang.setText(otomatis);
        }else {
            txtidtang.setText("1");
        }
        r.close();
        s.close();
    }catch(Exception e){
        System.out.print("EROR KUERI KE DATABASE:\n" + e + "\n\n");
    }
}
